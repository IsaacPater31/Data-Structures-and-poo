    private void jButton1ActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_jButton1ActionPerformed
        Records records1[]=new Records[5];
        IRecords r =new IRecords();
        try{
            File file = new File("data\\Data.csv"); //TENER ACCESO AL ARCHIVO
            BufferedReader br = new BufferedReader(new FileReader(file));//MANEJO MEMORIA
            String line; //VARIABLE PARA MANIPULAR CADA LINEA DEL ARCHIVO
            int i=0;
            br.readLine();
            while((line=br.readLine())!=null){
                String [] str = line.split(",");
                String Nombre = (str[0]);
                int scoree =Integer.parseInt(str[1]);
                int ballss = Integer.parseInt(str[2]);
                String Speed = str[3];
                records1[i] = new Records(Nombre, scoree, ballss, Speed);
                i++;
            }
        }
        catch(IOException | NumberFormatException y){
            System.out.println(y);
        }
        for (int x = 0; x < records1.length; x++) {
            r.jTextArea1.append("Nombre: "+records1[x].getName()+"\n");
            r.jTextArea1.append("Score: "+records1[x].getScore()+"\n");
            r.jTextArea1.append("Balls: "+records1[x].getBalls()+"\n");
            r.jTextArea1.append("SPEED: "+records1[x].getScore()+"\n");
            r.jTextArea1.append("==================================");
        }
        r.setVisible(true);
        r.setLocationRelativeTo(null);
    }//GEN-LAST:event_jButton1ActionPerformed
    public void setScore(int score,int minutes, int second, int milliseconds) {
        int finalScore=0;
        if(minutes==0){
            if(second==0){
                finalScore=milliseconds*score;
            }else{
                finalScore=second*milliseconds*score;
            }
        }else if(second==0){
            if(milliseconds==0){
                finalScore=minutes*score;
            }else{
                finalScore=minutes*milliseconds*score;
            }
        }else{
            finalScore=minutes*second*milliseconds*score;
        }
        jLabel4.setText("Su puntaje es de " + finalScore);
    }
    
     

